package decisionMaking;

public class SwitchDemo {

	public static void main(String[] args) {
		int x = 4;
		switch(x){
		case 1:
			System.out.println("poor performance");
		break;
		
		case 2:
			System.out.println("slow performance");
		break;
		
		case 3:
			System.out.println("meeting exceptations");
		break;
		
		case 4:
			System.out.println("Excellent keep on rocking");
		break;
		
		case 5:
			System.out.println("employee of the year");
		break;
		
		default:
			System.out.println("Nothing match");
		}

	}

}
